package bean;

public class CashShopModifiedItems {

   private int  serial;
   private int  showup;
   private int  itemid;

    public int getSerial() {
        return serial;
    }

    public void setSerial(int serial) {
        this.serial = serial;
    }

    public int getShowup() {
        return showup;
    }

    public void setShowup(int showup) {
        this.showup = showup;
    }

    public int getItemid() {
        return itemid;
    }

    public void setItemid(int itemid) {
        this.itemid = itemid;
    }
}
